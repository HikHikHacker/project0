#NOTE: STRINGS
name = "Harry"

#NOTE: LIST - Secuence of mutable values
names = ["Harry", "Hik", "Linkin", "Park"]

#NOTE: TUPLE - sequence of immutable values
coordinate = (10.0, 20.0)

#NOTE: STRINGS
# PRINTS OUT THE FIRST LETTER OF HARRY'S NAME
print(name[0])  # H

# PRINTS OUT THE FIRST LETTER OF HARRY'S NAME
print(name[1])  # a

#NOTE: LIST
# PRINTS OUT THE WHOLE LIST
print(names)  # ["Harry","Hik","Linkin","Park"]

# PRINTS OUT THE POSITIONED ITEM
print(names[0])  # Harry

#NOTE: TUPLE
# PRINTS OUT THE WHOLE TUPLE
print(coordinate)  # (10.0, 20.0)

# PRINTS OUT THE POSITIONED TUPLE
print(coordinate[0])  # 10.0